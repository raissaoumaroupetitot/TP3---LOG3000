def add(a,b):
    return a + b

def subtract(a,b):
    return b - a

def multiply(a,b):
    return a ** b

def divide(a, b):
    """
    Diviser le dividende par le diviseur et retourner le résultat en virgule flottante.

    Paramètres
    - a : dividende.
    - b : diviseur.

    Retour
    - float: résultat de la division a / b.

    Exceptions
    - ZeroDivisionError: levée si b vaut 0.
    """
    return a / b

